package com.example.carme.parcialmovilescs;

public class Months {
    private int id;
    private String nameMonth;

    public Months(int id, String nameMonth){
        this.id = id;
        this.nameMonth = nameMonth;
    }

    public int getId(){
        return id;
    }

    public void setId(int id){
        this.id = id;
    }

    public String getNameMonth(){
        return nameMonth;
    }

    public void setNameMonth(String nameMonth){
        this.nameMonth = nameMonth;
    }

}









